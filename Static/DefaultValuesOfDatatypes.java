class DefaultValuesOfDatatypes{
    static byte Num1 ;
    static short Num2 ;
    static int Num3 ;
    static float Num4 ;
    static char Num5 ;
    static long Num6 ;
    static double Num7 ;
    static boolean Num8 ;
    public static void main(String[] args) {
        System.out.println("Byte : "+Num1);
        System.out.println("Short : "+Num2);
        System.out.println("Int : "+Num3);
        System.out.println("Float : "+Num4);
        System.out.println("Char : "+Num5);
        System.out.println("Long : "+Num6);
        System.out.println("Double : "+Num7);
        System.out.println("Boolean : "+Num8);
    }
}